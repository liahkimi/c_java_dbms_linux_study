package ex04_casting;

public class Parent {

	// 필드
	int parentData;

	// 생성자
	public Parent(int parentData) {
		super();
		this.parentData = parentData;
	}
	
	// 메소드
	void parentMethod1() {
		System.out.println("parent method1");
	}
		
	void parentMethod2() {
		System.out.println("parent method2");
	}
	
	
	
	
	
}
