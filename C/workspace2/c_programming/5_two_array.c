#include <stdio.h>
int main() {
    // 2차원 배열
    int ar[2][3] = {
       {1, 2, 3},
       {10, 20, 30}
    };

    printf("ar[0] : %d\n", ar[0]);
    printf("ar[0][0] : %d\n", ar[0][0]);
    printf("ar[0][1] : %d\n", ar[0][1]);
    printf("ar[0][2] : %d\n", ar[0][2]);
    printf("ar[1] : %d\n", ar[1]);
    printf("ar[1][0] : %d\n", ar[1][0]);
    printf("ar[1][1] : %d\n", ar[1][1]);
    printf("ar[1][2] : %d\n", ar[1][2]);






    return 0;
}